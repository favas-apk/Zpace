package com.project.zpace.model;

public class model_cat {

    private String name;

    private int type;


    public model_cat(String name, int type) {
        this.name = name;
        this.type = type;
    }


    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }
}
